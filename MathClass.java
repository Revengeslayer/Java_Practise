package cm.code;
/*
    java.util.Math

    public static double abs(double num) 絕對值
    public static double ceil(double num) 向上取整
    public static double floor(double num) 向下取整
    public static Long round(double num) 四捨五入

    Math.pi
 */
public class MathClass {
    public static void main(String[] args) {
        System.out.println(Math.abs(-0.25));
        System.out.println(Math.ceil(-0.25));
        System.out.println(Math.floor(-0.25));
        System.out.println(Math.round(-0.25));
        System.out.println(Math.PI);
    }
}
